                <h2 class="text-center"><u>Мероприятия по пропоганде культуры и ценности здорового и безопасного образа жизни</u></h2>

                <hr>

                <h2 class="text-center">«Я выбираю будущее»</h2>

                <h3>Вводная часть</h3>

                <h4><b>Приветствие участников тренинга</b></h4>

                <p>Создание атмосферы сплоченности и содружества.</p>
                <p>Представление  участников: каждый ученик встает и представляет своего соседа,
                    характеризуя его с положительной стороны.</p>
                <p>Ребята, наш урок будет проходить в атмосфере доверия и дружелюбия.
                    Мы собрались сегодня, чтобы выслушать друг друга,
                    и сказать добрые правильные слова.</p>

                <h4><b>Упражнение «Пожелание удачи»</b></h4>
                <p><b>Цель</b>: установление атмосферы дружбы и добра.</p>

                <p>На листе ватмана, который висит на доске, нарисовано солнышко.
                    Ведущий раздает изготовленные из бумаги «лучики солнца».
                    На них подростки записывают свои пожелания добра и прикрепляют к солнышку.</p>

                <p>С пожеланиями и добрыми словами мы продолжим наш урок. </p>

                <h3>Основная часть</h3>

                <h4><b>Тренинг «Я умею говорить нет»</b></h4>

                <p>Ребята, бывают ситуации, когда нужно сказать «нет».
                    Я буду задавать вам вопросы ваши ответы должны начинать со слов
                    «Нет, я отказываюсь, потому что……».</p>

                <p>Итак:</p>
                <ol>
                    <li>Ты пойдешь с нами курить?</li>
                    <li>Пойдем веселиться с нами, там будет весело и будет много пива?</li>
                    <li>Давай заберем этот велосипед, никто не увидит?</li>
                </ol>

                <p>Рефлексия: почему нельзя поддаваться на уговоры и совершать плохие поступки,
                    все учащиеся высказывают свое мнение.</p>

                <h4><b>Игра «Путаница»</b></h4>

                <p><b>Цель</b>: формирование навыков общения.</p>

                <p>Ребята, давайте встанем в круг. Каждый ученик берет руку того,
                    кто стоит напротив, и руку еще кого-то. Все участники образуют узел рук.
                    Задача состоит в том, чтобы совместно договориться решить,
                    как, не распуская рук распутаться.</p>

                <p><b>Рефлексия</b>: ребята, в ходе игры мы усвоили, как можно решать проблемы и трудности сообща, все вместе. Давайте каждый из вас расскажет, что ему понравилось в игре, а что нет.</p>
                <h4><b>Тренинг «Цветок жизни»</b></h4>
                <p>Ребята перед вами красивый цветок. Каждый лепесток этого цветка составляет,
                    какую то хорошую сторону жизни - здоровье, красота, счастливая семья,
                    хорошая работа, здоровые дети, путешествия, общение с друзьями и близкими.
                    Каждый из вас подойдет и выберет лепесток и ответит на вопрос.
                    А теперь вопрос каждому из вас:</p>

                <ol>
                    <li>Что человек потеряет, если будет употреблять пиво? (ответ учащегося и удаление лепестка)</li>
                    <li>Как измениться внешность девушки, если она будет много курить? (ответ)</li>
                    <li>Что не сможет делать человек, если он употребляет наркотики?</li>
                    <li>Будут ли его уважать друзья, если он употребляет спиртное? </li>
                    <li>Сможет ли человек создать семью, если он употребляет наркотики?</li>
                    <li>Сможет ли выйти замуж девушка, которая много употребляет спиртного? (ответы участников)</li>
                </ol>

                <p>Правильно ребята! Но посмотрите, что осталось от цветка?
                    Правильно, только черная пустота. Человек теряет все,
                    когда употребляет спиртное, сигареты, наркотики.</p>

                <h3>Заключение. Итог урока</h3>
                <p><b>Рефлексия</b>: ребята сегодня мы с вами усвоили что,
                    нужно беречь свое здоровье, свое счастливое будущее.
                    Употребление спиртного, наркотиков и табака вредит не только здоровью,
                    но и ломает жизнь человека навсегда. «Правила здоровья».</p>

                <ol>
                    <li>Ребята, пусть каждый из вас выскажет, почему нельзя употреблять спиртное?</li>
                    <li>Почему наркотики  ломают жизнь человека?</li>
                    <li>Какой вред здоровью причиняет курение?</li>
                </ol>

                <h4><u>Упражнение «Завершение урока».</u></h4>
                <p>Ребята, давайте встанем в круг и скажем друг другу хорошие добрые слова.</p>

