                <h4 class="text-center">Самоанализ урока</h4>

                <p>Данный урок «Осень: Одежда и обувь человека осенью» является плановым уроком по предмету «Окружающий природный мир».
                    Урок соответствует тематическому планированию и относится ко второму разделу «Осень».
                    В разделе «Осень» (с 24.09.2018 по 25.10.2018) - это урок-повторение и закрепление темы «Одежда и обувь человека осенью».
                    Ученица уже знакома с понятиями: теплая одежда; приметы осени; сезонные изменения  природе.
                    Предварительно была проведена словарная работа с иллюстрациями над произношением  звуков.</p>

                <p>Исходя из этого, были определены цель и задачи данного урока:</p>

                <p><strong>Цель</strong>: формирование и закрепление представлений о природе и сезонных изменениях;</p>

                <strong>Задачи урока:</strong>

                <p><i>Образовательные:</i></p>
                <ul>
                    <li>освоить умение работать с природным материалом;</li>
                    <li>повторить и закрепить знания о сезонных изменениях в природе;</li>
                    <li>уметь ориентироваться на листе картона: вверх-низ;</li>
                    <li>обогащать словарный запас.</li>
                </ul>

                <p><i>Коррекционно-развивающие:</i></p>
                <ul>
                    <li>развивать коммуникативные навыки общения;</li>
                    <li>развивать произвольное внимание;</li>
                    <li>развивать мелкую и крупную моторику.</li>
                </ul>

                <p><i>Воспитательные:</i></p>
                <ul>
                    <li>воспитание культуры общения;</li>
                    <li>воспитание экологической культуры.</li>
                </ul>

                <p>Использовались следующие ресурсы:</p>
                <ol>
                    <li>АООП по предмету «Окружающий природный мир» для учащихся с умеренной и тяжелой умственной отсталостью, под редакцией Л. Б. Баряевой.</li>
                    <li>Школьные принадлежности, лист картона А4 с нарисованным силуэтом дерева,</li>
                    <li>Сухие листья шиповника и акации, </li>
                    <li>Бумажная кукла с набором одежды;</li>
                    <li>Шарики «су-джок»;</li>
                    <li>Иллюстрация «Осень».  Демонстрационные картинки (набор из 12 картинок) 2-е издание.; Сфера - М., 2016. </li>
                </ol>

